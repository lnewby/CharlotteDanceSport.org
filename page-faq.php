<h3 id="q11">What's the difference between International and American Ballroom?</h3>
<h2>International Ballroom</h2>
<p>This style consists of Standard (Waltz, Tango, Foxtrot, Viennese Waltz, Quickstep) and Latin (Samba, Cha Cha, Rumba, Paso Doble, Jive).  Standard and Latin have a single syllabus that is the same throughout the world.  This makes it more conducive for competitive dancing.</p>
<h2>American Ballroom</h2>
<p> This style consists of Smooth (Waltz, Tango, Foxtrot, Viennese Waltz) and Rhythm (Cha Cha, Rumba, East Coast Swing, Bolero, Mambo).  American is danced primarily in the U.S. and there is more than one syllabus.  This makes it harder to judge.  It is geared more towards social dancing.  The club focuses on International Ballroom.</p>
<ul><li><a class="top_faq" >&and; top</a></li></ul>

