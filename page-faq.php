<h3 id="q5">What's the difference between Social, Newcomer Team, Bronze Team, and Silver Team?</h3>
<ul>
<li><strong>Social</strong> You will learn enough basic steps to be able to get out on the dance floor and dance to music.  This is open to anybody and is free.  Social is taught by advanced members of the club.</li>
<li><strong>Newcomer Team</strong> You will continue to learn what was taught in Social, with the addition of technique for competitive dancing being introduced.  This is open to anybody.  Semester dues are $25 for students and $45 for non-students.  Typically people join the Newcomer team with the intent of competing but you do not have to compete (although it is encouraged).  These lessons are taught by the advanced members of the club.</li>
<li><strong>Bronze Team</strong> You will learn the entire Bronze syllabus and more advanced technique.  The lessons are geared toward competitive dancing.  Semester dues are $80 for students and $120 for non-students.  In order to join the Bronze Team a tryout is required.  These lessons are taught by professional coaches.</li>
<li><strong>Silver Team</strong> This is the same as Bronze except the Silver syllabus is taught.  The dues are the same and a tryout is required.  These lessons are also taught by professional coaches.</li>
</ul>
